// カテゴリごとにフィルタ
export function getQuestionsByCategory(category: CalcCategory): CalcQuestion[] {
  return calcQuestions.filter((q) => q.category === category);
}

// ランダムに1問取得
export function getRandomQuestion(category?: CalcCategory): CalcQuestion {
  const pool = category ? getQuestionsByCategory(category) : calcQuestions;
  return pool[Math.floor(Math.random() * pool.length)];
}
